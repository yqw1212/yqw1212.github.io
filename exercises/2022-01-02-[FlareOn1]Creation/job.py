def rol(a,b,c=0):
    a=bin(a)[2:]
    l=[]
    for i in range(8-len(a)):
        l.append('0')
    for i in a:
        l.append(i)

    for i in range(b):
        t=l[0]
        for j in range(len(l)-1):
            l[j]=l[j+1]
        l[len(l)-1]=t
    a=''
    for i in l:
        a+=i
    return(chr((int(a,2)-c)&0xff))

def ror(a,b,c=0):
    a=bin(a)[2:]
    l=[]
    for i in range(8-len(a)):
        l.append('0')
    for i in a:
        l.append(i)

    for i in range(b):
        t=l[len(l)-1]
        for j in range(len(l)-1,0,-1):
            l[j]=l[j-1]
        l[0]=t
    a=''
    for i in l:
        a+=i
    return(chr((int(a,2)-c)&0xff))

