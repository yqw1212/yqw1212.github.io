from secret import flag
from Crypto.Util.number import *

seed = bytes_to_long(flag)
bits = seed.bit_length()

while True:
    p = getPrime(bits + 1)
    if p > seed:
        break
print(p)

a = getRandomRange(1, p)
b = getRandomRange(1, p)

for _ in range(3):
    seed = (a * seed + b) % p
    print(seed)

